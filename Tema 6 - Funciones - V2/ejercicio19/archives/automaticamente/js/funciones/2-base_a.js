function binarioDecimal(numeroBinario) {
  // Convertir de binario a decimal
  return parseInt(numeroBinario, 2);
}

function hexadecimalDecimal(numeroHexadecimal) {
  // Convertir de hexadecimal a decimal
  return parseInt(numeroHexadecimal, 16);
}

function octalDecimal(numeroOctal) {
  // Convertir de octal a decimal
  return parseInt(numeroOctal, 8);
}
